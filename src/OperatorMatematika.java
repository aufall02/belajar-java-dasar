public class OperatorMatematika {
    public static void main(String[] args) {
        var  a =  100;
        var  b =  10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a*b);

        //augmnted assigment

        var c = 100;

        c += 10;
        System.out.println(c);
        c -= 10;
        System.out.println(c);
        c *= 10;
        System.out.println(c);

        //unary Operator

        var d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);



    }
}
